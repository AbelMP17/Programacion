    private void rellenarMarcas(){
        Set<String> setPr = new HashSet<>();
        for(int i = 0; i<productos.size();i++){
            setPr.add(productos.get(i).marca());
        }
        
        for(String i : setPr){
            cmbMarcas.addItem(i);
        }
    }

    private void rellenarListaProductos(){
        Producto[] prList = {new Producto("Patata", "Hacendado", 6),
                            new Producto("Pan", "Hacendado", 4),
                            new Producto("Zumo", "Coviran", 2),
                            new Producto("Galletas", "Hacendado", 2),
                            new Producto("Refresco", "Hlanz", 6),
                            new Producto("Escoba", "Hlanz", 3)};
        productos = new DefaultListModel<>();
        for(int i = 0; i<prList.length ;i++){
            productos.addElement(prList[i]);
        }
    }
    
    
