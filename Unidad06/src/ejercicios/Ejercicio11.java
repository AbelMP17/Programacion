    private void conversion(){
        if(txtEuros.getText().matches("^\\d+(\\.\\d{2})?$")){
            double cant = Double.parseDouble(txtEuros.getText());

            if(chkDolares.isSelected()){
                //Conversion a dolares
                double dol = cant*1.06;
                NumberFormat nf1 = NumberFormat.getCurrencyInstance(Locale.US);
                txtEuros.setText(nf1.format(dol));
            }else if(chkLibras.isSelected()){
                //Conversion a libras
                double lib = cant*0.88;
                NumberFormat nf2 = NumberFormat.getCurrencyInstance(Locale.UK);
                txtEuros.setText(nf2.format(lib));
            }else if(chkYenes.isSelected()){
                //Conversion a yenes
                double yenes = cant*144.92;
                NumberFormat nf3 = NumberFormat.getCurrencyInstance(Locale.JAPAN);
                txtEuros.setText(nf3.format(yenes));
            }else if(chkPesetas.isSelected()){
                //Conversion a pesetas
                double pst = cant*166.386;
                NumberFormat dF = new DecimalFormat("#.## pts");
                txtEuros.setText(dF.format(pst));
            }else{
                JOptionPane.showMessageDialog(this, "Debes seleccionar una checkbox.", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Numero invalido, debe tener dos decimales", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
